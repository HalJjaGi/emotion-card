// 랜덤 변형 적용 (30% 확률로 변경)
function applyVariation(pattern: number[][]): number[][] {
  return pattern.map(row =>
    row.map(cell => (Math.random() < 0.3 ? 1 - cell : cell))
  );
}

// 도트 아트 생성
export function generateDotArt(emotionName: string): string {
  const basePattern = EMOTION_PATTERNS[emotionName] || EMOTION_PATTERNS['기쁨'];
  const pattern = applyVariation(basePattern);

  // ASCII art로 변환
  return pattern
    .map(row =>
      row
        .map(cell => (cell === 1 ? '●' : '○'))
        .join('')
    )
    .join('\n');
}
